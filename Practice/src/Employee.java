
public class Employee 
{
 private String name;
 private int salary;
 private String Add;
 private int id ;
 

public void setName(String name) {
	this.name = name;
	}
public String getName() {
	return name;
}
public int getSalary() {
	return salary;
}
public void setSalary(int salary) {
	this.salary = salary;
}
public String getAdd() {
	return Add;
}
public void setAdd(String add) {
	Add = add;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}

 
 
}
